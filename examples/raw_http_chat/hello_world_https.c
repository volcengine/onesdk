int global_data_len = 0; // 接受数据总数
int receive_batch  = 0;//回调次数
void on_data_received(const char *body, size_t len, bool last_chunk, void *user_data) {
    global_data_len += len;
    receive_batch += 1;
    printf("body: %s\n", body);
    printf("round: %d => len: %d, total: %d\n", receive_batch, len, global_data_len);
    printf("user_data: %p\n", user_data);

}

void on_error(int error_code,char *msg, void *user_data) {
    printf("on_error: %d, msg: %s\n", error_code, msg);
}


